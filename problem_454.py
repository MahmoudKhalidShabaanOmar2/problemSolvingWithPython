# write a python program to get the dictionary of the user information , and then get the total length of the keys , and values of the dictionary =>
dictionary_of_user_information = {"name" : "Mahmoud Khalid" , "age" : "23 Years Old" , "gender" : "male"}
print("The Dictionary Of The User Information Is : ",str(dictionary_of_user_information))
length_of_keys = len(dictionary_of_user_information.keys())
print("The Length Of The Keys Of The Dictionary of The User Information Is = ",str(length_of_keys)," Keys")
length_of_values = len(dictionary_of_user_information.values())
print("The Length of The Values Of The Dictionary Of The User Information Is = ",str(length_of_values)," Values")