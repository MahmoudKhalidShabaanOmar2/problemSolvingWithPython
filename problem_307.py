# write a python program to get the volume of an object through density =>
massOfObject = int(input("Please Enter The Mass Of An Object Is = "))
densityOfObject = int(input("Please Enter The Density Of An Object Is = "))
print("The Mass Of An Object Is = ",massOfObject)
print("The Density Of An Object Is = ",densityOfObject)
volumeOfObject = densityOfObject * massOfObject 
print("The Volume Of An Object Is = ",volumeOfObject)