# write a python program to get density of an object => 
massOfObject = float(input("Please Enter The Mass Of The Object Is = "))
volumeOfObject = float(input("Please Enter The Volume Of The Object Is = "))
print("The Mass Of An Object Is = ",str(massOfObject))
print("The Volume Of An Object Is = ",str(volumeOfObject))
densityOfObject = massOfObject / volumeOfObject 
print("The Density Of An Object Is = ",densityOfObject)